# G√∂revleri dosyadan okuma
def gorevleri_yukle():
    try:
        with open("gorevler.txt", "r", encoding="utf-8") as dosya:
            return [satir.strip() for satir in dosya.readlines()]
    except FileNotFoundError:
        return []

# G√∂revleri dosyaya kaydetme
def gorevleri_kaydet(gorevler):
    with open("gorevler.txt", "w", encoding="utf-8") as dosya:
        for gorev in gorevler:
            dosya.write(gorev + "\n")

# G√∂revleri g√∂ster
def gorevleri_goster(gorevler):
    if not gorevler:
        print("üì≠ Hi√ß g√∂rev yok.")
    else:
        print("\nüìã G√∂rev Listesi:")
        for i, g in enumerate(gorevler, 1):
            print(f"{i}. {g}")

# G√∂rev ekle
def gorev_ekle(gorevler):
    yeni_gorev = input("‚úçÔ∏è Yeni g√∂rev girin: ").strip()
    if yeni_gorev:
        gorevler.append(yeni_gorev)
        gorevleri_kaydet(gorevler)
        print(f"‚úÖ G√∂rev eklendi: {yeni_gorev}")
    else:
        print("‚ö†Ô∏è Bo≈ü g√∂rev eklenemez.")

# G√∂rev sil
def gorev_sil(gorevler):
    gorevleri_goster(gorevler)
    try:
        index = int(input("Silinecek g√∂rev numarasƒ±: ")) - 1
        if 0 <= index < len(gorevler):
            silinen = gorevler.pop(index)
            gorevleri_kaydet(gorevler)
            print(f"üóëÔ∏è Silindi: {silinen}")
        else:
            print("‚ùå Ge√ßersiz g√∂rev numarasƒ±.")
    except ValueError:
        print("‚ùå L√ºtfen ge√ßerli bir sayƒ± girin.")

# G√∂rev d√ºzenle
def gorev_duzenle(gorevler):
    gorevleri_goster(gorevler)
    try:
        index = int(input("D√ºzenlenecek g√∂rev numarasƒ±: ")) - 1
        if 0 <= index < len(gorevler):
            yeni_gorev = input("Yeni g√∂rev: ").strip()
            if yeni_gorev:
                eski = gorevler[index]
                gorevler[index] = yeni_gorev
                gorevleri_kaydet(gorevler)
                print(f"‚úèÔ∏è G√ºncellendi: {eski} ‚û°Ô∏è {yeni_gorev}")
            else:
                print("‚ö†Ô∏è Bo≈ü g√∂rev girilemez.")
        else:
            print("‚ùå Ge√ßersiz g√∂rev numarasƒ±.")
    except ValueError:
        print("‚ùå L√ºtfen ge√ßerli bir sayƒ± girin.")

# Ana program d√∂ng√ºs√º
